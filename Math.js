// Math Function

// Minimum Value
const MinimumValue = Math.min(2, 5, 10, -1, 0, 16, -27);
console.log('Minimum Value is: ', MinimumValue);

//Maximum Value
const MaximumValue = Math.max(2, 5, 10, -1, 0, 16, -27);
console.log('Maximum Value is: ', MaximumValue);

//Square Root
const squareRoot = Math.round(Math.sqrt(6));
console.log('Square root is: ', squareRoot);

const square = (a) => {
  return a * a;
};

console.log('Square is: ', square(9));
